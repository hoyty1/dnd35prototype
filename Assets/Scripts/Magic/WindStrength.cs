/// <summary>
/// D&D 3.5e wind strength categories used for wind-based interactions.
/// </summary>
public enum WindStrength
{
    None = 0,
    Light = 1,      // 0-10 mph
    Moderate = 2,   // 11-20 mph
    Strong = 3,     // 21-30 mph
    Severe = 4,     // 31-50 mph
    Windstorm = 5   // 51+ mph
}
